package ru.tensor.sbis.design.theme

/**
 * Выравнивание по горизонтали контента в компоненте.
 *
 * @author mb.kruglova
 */
enum class HorizontalAlignment {

    /**
     * Размещение контента слева.
     */
    LEFT,

    /**
     * Размещение контента по центру.
     */
    CENTER,

    /**
     * Размещение контента справа.
     */
    RIGHT
}