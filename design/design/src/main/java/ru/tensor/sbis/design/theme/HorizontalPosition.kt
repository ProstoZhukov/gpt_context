package ru.tensor.sbis.design.theme

/**
 * Положение элемента/группы элементов в компоненте относительно другого элемента/группы элементов в горизонтали.
 *
 * @author mb.kruglova
 */
enum class HorizontalPosition {

    /**
     * Элемент/группа элементов располагается слева относительно другого элемента/группы элементов.
     */
    LEFT,

    /**
     * Элемент/группа элементов располагается справа относительно другого элемента/группы элементов.
     */
    RIGHT
}