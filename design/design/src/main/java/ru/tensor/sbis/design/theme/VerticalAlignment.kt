package ru.tensor.sbis.design.theme

/**
 * Выравнивание по вертикали контента в компоненте.
 *
 * @author mb.kruglova
 */
enum class VerticalAlignment {

    /**
     * Размещение контента сверху.
     */
    TOP,

    /**
     * Размещение контента по центру.
     */
    CENTER,

    /**
     * Размещение контента снизу.
     */
    BOTTOM
}