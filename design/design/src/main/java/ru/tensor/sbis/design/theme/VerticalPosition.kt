package ru.tensor.sbis.design.theme

/**
 * Положение элемента/группы элементов в компоненте относительно другого элемента/группы элементов в вертикали.
 *
 * @author mb.kruglova
 */
enum class VerticalPosition {

    /**
     * Элемент/группа элементов располагается сверху относительно другого элемента/группы элементов.
     */
    TOP,

    /**
     * Элемент/группа элементов располагается снизу относительно другого элемента/группы элементов.
     */
    BOTTOM
}