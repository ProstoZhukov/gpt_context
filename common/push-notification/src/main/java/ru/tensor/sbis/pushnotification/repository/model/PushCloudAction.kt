package ru.tensor.sbis.pushnotification.repository.model

/**
 * Типы дейтствий с пуш уведомлением, которые может инициировать облако при поступлении пуша на устройство.
 *
 * @author am.boldinov
 */
enum class PushCloudAction {
    NOTIFY, // уведомляет пользователя о новом уведомлении (показать пуш)
    CANCEL, // отменяет полученное ранее уведомление (удалить пуш)
    UPDATE, // обновляет полученнное ранее уведомление (обновить информацию в пуше)
}
